from enum import Enum


class Event(Enum):
    """Event enumeration."""
    NONE = 'NONE'
    ALIGNMENT = 'ALIGNMENT'
    CLOSE = 'CLOSE'
    DELETE = 'DELETE'
    DRAG = 'DRAG'
    DROP = 'DROP'
